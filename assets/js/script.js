function crearPropiedad(_propiedad) {
    /* creacion div principal */
    const nuevo = document.createElement('div');
    nuevo.className = 'propiedad';
    /* creacion imagen */
    const img_contenedor = document.createElement('div');
    img_contenedor.className = 'img';

    const img = document.createElement('img');
    img.src = _propiedad.src;

    img_contenedor.appendChild(img);
    nuevo.appendChild(img_contenedor);

    /* Creacion de div informacion */
    const informacion = document.createElement('div');
    informacion.className = 'informacion';

    /* creacion de nombre */
    const name = document.createElement('h3');
    name.innerText = _propiedad.nombre;
    informacion.appendChild(name);

    /* Creacion de descripcion */
    const descripcion = document.createElement('h5');
    descripcion.innerText = _propiedad.descripcion;
    informacion.appendChild(descripcion);

    /* creacion de ubicacion con icono */
    const ubicacion = document.createElement('h5');
    const icono_ubicacion = document.createElement('i');
    icono_ubicacion.className = 'fa-solid fa-location-dot';
    ubicacion.appendChild(icono_ubicacion);
    ubicacion.append(_propiedad.ubicacion);
    informacion.appendChild(ubicacion);

    /* creacion de dormitorios y baños */
    const habBan =  document.createElement('h5');

    const icono_cama = document.createElement('i');
    icono_cama.className = 'fa-solid fa-bed';
    habBan.appendChild(icono_cama);

    let habitacion;

    if(_propiedad.habitaciones === 1) {
        habitacion = `${_propiedad.habitaciones} Habitación | `;
    } else {
        habitacion = `${_propiedad.habitaciones} Habitaciones | `;
    }
        
    habBan.append(habitacion);

    const icono_bano = document.createElement('i');
    icono_bano.className = 'fa-solid fa-bath';

    habBan.append(icono_bano);

    let banos;

    if(_propiedad.bano === 1) {
        banos = `${_propiedad.bano} Baño`;
    } else {
        banos = `${_propiedad.bano} Baños`;
    }

    habBan.append(banos);
    informacion.appendChild(habBan);

    const price = document.createElement('h2');

/* fomtear el precio */
    price.innerText = new Intl.NumberFormat('es-CL', { style: 'currency', currency: 'CLP' }).format(
        _propiedad.costo
      ) ;

    informacion.appendChild(price);

    const petHtml = document.createElement('h5');

    if(_propiedad.pets === true) {
        petHtml.className = 'pet';
        const icon_pet = document.createElement('i');
        icon_pet.className = 'fa-solid fa-paw';
        petHtml.appendChild(icon_pet);
        petHtml.append('Mascotas permitidas');
    } else {
        petHtml.className = 'not-pet';
        const icon_pet = document.createElement('i');
        icon_pet.className = 'fa-solid fa-ban';
        petHtml.appendChild(icon_pet);
        petHtml.append('No se permiten mascotas');
    }

    informacion.appendChild(petHtml);

    const smokeHtml = document.createElement('h5');

    if(_propiedad.smoke === true) {
        smokeHtml.className = 'smoke';
        const icon_pet = document.createElement('i');
        icon_pet.className = 'fa-solid fa-smoking';
        smokeHtml.appendChild(icon_pet);
        smokeHtml.append('Permitido fumar');
    } else {
        smokeHtml.className = 'not-smoke';
        const icon_pet = document.createElement('i');
        icon_pet.className = 'fa-solid fa-ban-smoking';
        smokeHtml.appendChild(icon_pet);
        smokeHtml.append('No se permite fumar');
    }

    informacion.appendChild(smokeHtml);
    nuevo.appendChild(informacion);

    return nuevo;
}

